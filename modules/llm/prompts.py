# PersonaProfile (하위 호환 래퍼)
# ============================================================================

@dataclass
class PersonaProfile:
    """작성자 페르소나 (단일 캐릭터 + 주제 모드 래퍼)."""
    id: str
    name: str
    description: str
    expertise: List[str]
    writing_style: str
    default_tone: str
    prompt_prefix: str  # CHARACTER + 주제 모드 컨텍스트 결합
    topic_mode: str = "cafe"


def _build_persona_from_topic(topic: TopicMode) -> PersonaProfile:
    """TopicMode → PersonaProfile 변환 (하위 호환용)."""
    combined_prefix = (
        _CHARACTER_BACKGROUND
        + "\n\n"
        + _QUALITY_INDEPENDENCE_NOTE
        + "\n\n"
        + topic.topic_context.strip()
    )
    if topic.id == "finance":
        persona_name = "투자를 공부하는 아빠"
    elif topic.id == "cafe":
        persona_name = "카페사장 아빠"
    elif topic.id == "health":
        persona_name = "건강을 공부하는 아빠"
    else:
        persona_name = f"공부하는 아빠 ({topic.name})"
    return PersonaProfile(
        id=topic.id,
        name=persona_name,
        description=topic.description,
        expertise=topic.keywords_hint,
        writing_style="성장형 1인칭, 겸손하면서도 실용적인",
        default_tone=topic.default_tone,
        prompt_prefix=combined_prefix,
        topic_mode=topic.id,
    )


def get_tone_profile(tone_name: str = "conversational") -> ToneProfile:
    """톤 프로필을 반환한다."""
    return TONE_PROFILES.get(tone_name, TONE_PROFILES["conversational"])


_TOPIC_ALIAS_MAP: Dict[str, str] = {
    "economy": "finance",
}


def normalize_topic_mode(topic_id: str = "cafe") -> str:
    """토픽 모드를 정규화한다 (별칭 지원)."""
    raw_value = str(topic_id).strip()
    if not raw_value:
        return "cafe"
    normalized = _LEGACY_PERSONA_MAP.get(raw_value, raw_value)
    normalized = _LEGACY_PERSONA_MAP.get(normalized.upper(), normalized)
    normalized = str(normalized).lower()
    return _TOPIC_ALIAS_MAP.get(normalized, normalized)


def get_persona_profile(persona_id: str = "cafe") -> PersonaProfile:
    """페르소나(주제 모드) 프로필을 반환한다.

    persona_id는 토픽 모드 ID (cafe, parenting, it, finance, economy) 또는
    레거시 ID (P1~P4)를 모두 지원한다.
    """
    resolved_id = normalize_topic_mode(persona_id)
    topic = TOPIC_MODES.get(resolved_id, TOPIC_MODES["cafe"])
    return _build_persona_from_topic(topic)


def get_topic_mode(topic_id: str = "cafe") -> TopicMode:
    """주제 모드를 직접 반환한다."""
    resolved_id = normalize_topic_mode(topic_id)
    return TOPIC_MODES.get(resolved_id, TOPIC_MODES["cafe"])


